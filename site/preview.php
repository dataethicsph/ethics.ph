            <div class="w3-row" style="font-size: 1.1em; line-height: 1.6;">
                <h3 class="w3-text-deep-orange">What is AI Ethics?</h3>
                <p>AI Ethics considers questions about fairness, transparency, privacy, and the impact of AI systems on human well-being and society as a whole.</p>

                <h3 class="w3-text-deep-orange w3-margin-top">Why is it important for AI to be ethical?</h3>
                <p>AI-powered systems, processes, and algorithms already affect our lives whether we are aware of them or not. As the technology becomes more powerful and pervasive we need to work to ensure that it is developed responsibly and ethically.</p>
                <p>If AI isn’t built well, it could lead to privacy violations, unfairness in systems, and inequitable or anti-democratic development–just to name a few of the many possible challenges. At the same time, Data and AI Ethics PH advocates for ethical AI so that we can use the technology to build more equitable communities, workplaces, culture, and society. Ethical AI will empower people to accomplish things they couldn’t do before.</p>

                <h3 class="w3-text-deep-orange w3-margin-top">How do I benefit from ethical AI?</h3>
                <p>Ethical AI benefits you in both direct and indirect ways:</p>
                <ul>
                    <li><strong>Direct benefits</strong> include fairer treatment when AI systems make decisions about your life, better protection of your personal data, and more transparency about how AI affects you. For instance, when applying for a job, ethical AI practices ensure your application is evaluated fairly and that you can understand why certain decisions were made.</li>
                    <li><strong>Indirect benefits</strong> come from living in a society where AI systems work for the common good. This includes potentially more reliable public services, reduced discrimination in institutional systems, and technological advancement that respects human rights and promotes social good.</li>
                </ul>

                <h3 class="w3-text-deep-orange w3-margin-top">What does it take to make AI ethical?</h3>
                <p>Making AI ethical requires a comprehensive approach that combines technical solutions with human oversight and governance:</p>
                <ul>
                    <li><strong>Technical requirements</strong> include developing algorithms that are fair and unbiased, implementing robust privacy protections, and creating systems that can explain their decisions.</li>
                    <li><strong>Human elements</strong> include diverse teams working on AI development, ongoing consultation with affected communities, and clear accountability structures.</li>
                    <li><strong>Organizational practices</strong> need to include strong governance frameworks, regular ethical impact assessments, and continuous monitoring of AI systems' effects on people and society.</li>
                </ul>

                <h3 class="w3-text-deep-orange w3-margin-top">What’s the difference between Data ethics and AI ethics?</h3>
                <p>Initially founded as Data Ethics PH, we rebranded to Data and AI Ethics PH to reflect the increasing importance of AI. While Data ethics and AI ethics are closely related and often overlap, they focus on different aspects of technology use:</p>
                <p>Data ethics focuses on the responsible collection, storage, and use of data. AI ethics builds upon data ethics but goes further to address the specific challenges of artificial intelligence systems. For instance, AI ethics would consider not just how facial recognition data is collected (a data ethics concern), but also whether facial recognition should be used for surveillance at all, and what limits should be placed on such systems.</p>
                <p>Data Ethics and AI ethics are inextricably linked. There’s no AI without data. But it adds its own opportunities, complications, and challenges. We believe AI is going to be a truly transformative technology and it’s essential that we develop ethical frameworks to guide us through its development, adaptation, and evolution.</p>
                <p>Beyond the technology itself, we at Data and AI Ethics PH see the need to be at the forefront of ethical discussions on AI with our specific perspective coming from the Philippines. So much of the technology is being developed abroad, but we have such high adoption of it here. The threat of AI Colonialism alongside the opportunities of Sovereign AI will be at the forefront of our thinking, discussions, and advocacy.</p>
