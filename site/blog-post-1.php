        <div class="w3-large" style="line-height: 1.8;">
            <p>Following reports that the Cybercrime Investigation and Coordinating Center (CICC) is considering restricting access to the messaging platform Signal on the basis of alleged spoofing incidents, we at Data and AI Ethics PH express our strong condemnation of any such action that amounts to prior restraint on the fundamental human right to free expression in the digital sphere.</p> 

            <p>Signal is a messaging platform widely recognized for its end-to-end encryption, making it an effective tool for users who depend on secure communications in their personal and professional lives, such as journalists, lawyers, and human rights defenders.</p> 

            <p>Proposing a ban on Signal solely on the basis of alleged spoofing incidents shows that the CICC is missing the broader context of the problem. Such a blanket restriction is an overbroad and disproportionate response to an issue that calls for targeted enforcement. Efforts should instead focus on the threat actors and the mechanisms of fraud, not on restricting the secure infrastructure that protects legitimate users.</p> 

            <p>More troublingly, such a ban would set a dangerous precedent. It normalizes the notion that privacy‑preserving technologies, like end-to-end encryption, may be restricted whenever they are perceived as inconvenient to enforcement efforts, rather than when there is clear evidence of legitimate necessity. This risks steering the country toward a regulatory environment where surveillance is favored over security; where control is equated with effective governance.</p>

            <p>We fully recognize the legitimate threat that cybercrimes such as spoofing pose to public safety. However, efforts to address these harms must be grounded in clear, demonstrable evidence and must exhaust less intrusive alternatives before considering any regulation that limits access to communication services.</p>

            <p><strong>Protecting Filipino citizens from fraud should not come at the cost of their fundamental human rights.</strong> We call for a balanced, rights-based approach that targets criminal conduct directly while preserving the integrity of essential digital infrastructure.</p>
